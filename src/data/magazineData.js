// Helper functions for magazine data
export const getMagazineById = (id) => {
  return MAGAZINES[id] || null;
};

export const getAllMagazines = () => {
  return Object.values(MAGAZINES);
};

export const getFeaturedMagazines = () => {
  return Object.values(MAGAZINES).filter(magazine => magazine.featured);
};

export const getMagazineIds = () => {
  return Object.keys(MAGAZINES);
};

export const getLatestMagazine = () => {
  const magazines = Object.values(MAGAZINES);
  // Sort by release date (assuming newer issues have higher IDs for now)
  return magazines.sort((a, b) => parseInt(b.id) - parseInt(a.id))[0];
};

export const getMagazineCount = () => {
  return Object.keys(MAGAZINES).length;
};

// Validation helper
export const isValidMagazineId = (id) => {
  return Object.prototype.hasOwnProperty.call(MAGAZINES, id);
};

